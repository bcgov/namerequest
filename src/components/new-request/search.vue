  <v-container fluid id="search-container" class="copy-normal pt-10 px-10"
    :class="(isFederal && isRestoration) ? 'pb-0' : 'pb-12'"
  >
    <header class="h6">Get started by selecting an action:</header>

    <v-row class="pt-6">
      <!-- Request Action -->
      <RequestAction />

      <!-- display a dummy input box when Request Action is not yet selected -->
      <DummyInputBox v-if="!getSearchRequest" />

      <!-- New BC Business flow -->
      <template v-if="isNewBcBusiness">
        <EntityType v-if="getLocation" />
        <CompanyType v-if="entity_type_cd" />
        <NumberedCompanyBullets v-if="isNumberedCompany" />

        <template v-if="isNamedCompany">
          <v-col cols="12" :md="showDesignation ? '8' : '12'">
            <NameInput
              :is-mras-search="(isXproFlow && isMrasJurisdiction && !getHasNoCorpNum)"
              @emit-corp-num-validity="corpNumValid = $event"
            />
          </v-col>
          <Designation cols="12" md="4" />
        </template>
      </template>

      <!-- New Xpro Business flow -->
      <template v-else-if="isNewXproBusiness">
        <EntityType v-if="getLocation && !isFederal" cols="12" md="4" />
        <Jurisdiction />
        <CompanyType v-if="showCompanyTypeRadioButtons" />
      </template>

      <!-- Continuation In flow -->
      <template v-else-if="isContinuationIn">
        <EntityType v-if="getLocation" />
        <CompanyType v-if="showCompanyTypeRadioButtons" />
      </template>

      <!-- Change Name flow -->
      <template v-else-if="isChangeName">
        <BusinessLookupFetch />
        <Jurisdiction v-if="isChangeNameXpro" />
        <CompanyType v-if="showCompanyTypeRadioButtons" />
      </template>

      <!-- Amalgamation flow -->
      <template v-else-if="isAmalgamation">
        <EntityType v-if="getLocation && !isFederal" />

        <template v-if="isXproEntityType(getEntityTypeCd)">
          <Jurisdiction />
        </template>

        <template v-else>
          <CompanyType v-if="showCompanyTypeRadioButtons" />
        </template>
      </template>

      <!-- Conversion (aka Alteration) flow -->
      <template v-else-if="isConversion">
        <EntityType v-if="getSearchBusiness && isAlterable" />
        <BusinessLookupFetch />
        <CompanyType v-if="showCompanyTypeRadioButtons" />
      </template>

      <!-- Restoration / Reinstatement flow -->
      <template v-else-if="isRestoration">
        <EntityType cols="12" md="6" />
        <BusinessLookupFetch />
        <Jurisdiction v-if="isSelectedXproAndRestorable" cols="12" md="4" />
        <CompanyType v-if="showCompanyTypeRadioButtons" />
      </template>
    </v-row>

    <!-- Xpro/Federal bullets -->
    <!-- *** TODO: move this into flows above -->
    <!-- *** TODO: simplify this button's logic -->
    <template v-if="entity_type_cd || isFederal || isRestorable">
      <v-row v-if="isNamedCompany || isAmalgamation">
        <XproFederalBullets v-if="isXproFlow && isFederal" cols="12" />
      </v-row>
    </template>

    <!-- Corporate Number checkbox, only for Canadian MRAS jurisdictions -->
    <!-- *** TODO: move this into flows above -->
    <!-- *** TODO: simplify this button's logic -->
    <template v-if="entity_type_cd || isFederal || isRestorable">
      <v-row v-if="isNamedCompany || isAmalgamation">
        <v-col cols="12" class="d-flex justify-end">
          <CorpNumberCheckbox v-if="isCanadian && isMrasJurisdiction" />
        </v-col>
      </v-row>
    </template>

    <!-- Go to COLIN button -->
    <template v-if="showColinButton">
      <v-row justify="center" class="mt-6">
        <v-col cols="auto">
          <v-btn id="go-to-colin-button" class="px-9" :href="colinLink" target="_blank">
            Go to Corporate Online to {{ isConversion ? 'Alter' : 'Register' }}
            <v-icon small class="ml-1">mdi-open-in-new</v-icon>
          </v-btn>
        </v-col>
      </v-row>
    </template>

    <!-- Incorporate Now button -->
    <template v-if="showActionButton">
      <v-row justify="center" class="mt-6">
        <v-col cols="auto">
          <v-btn id="incorporate-now-button" class="px-9" @click="actionNowClicked()">
            {{ actionNowButtonText }}
          </v-btn>
        </v-col>
      </v-row>
    </template>

    <!-- Check This Name button -->
    <template v-if="isShowCheckNameButton">
      <v-row justify="center" class="mt-6">
        <v-col cols="auto">
          <v-btn
            id="search-name-btn"
            class="px-9"
            :class="{ 'mobile-btn' : isMobile }"
            :disabled="!corpNumValid"
            @click="handleSubmit(true)"
          >
            <v-icon left color="white" size="1.5rem">mdi-magnify</v-icon>
            Check this Name
          </v-btn>
        </v-col>
      </v-row>

      <v-row v-if="isPremiumOrStaff" justify="center">
        <v-col cols="auto">
          <v-btn id="name-check-skip-btn" class="outlined pa-0" :ripple="false" text @click="handleSubmit(false)">
            <span>Submit this Name without checking</span>
            <v-icon>mdi-chevron-right</v-icon>
          </v-btn>
        </v-col>
      </v-row>
    </template>
  </v-container>
